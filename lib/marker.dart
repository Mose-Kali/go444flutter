class MarkerItem {
  String Name = "";
  String Tele = "";
  String Place = "";
  String Icon = "";
  double latitude = 0.0;
  double longtitude = 0.0;
  MarkerItem(String Name, String Place, String Tele, String Icon,
      double latitude, double longtitude) {
        Name = Name;
        Place = Place;
        Tele = Tele;
        Icon = Icon;
        latitude =latitude;
        longtitude=longtitude;
      }
}
